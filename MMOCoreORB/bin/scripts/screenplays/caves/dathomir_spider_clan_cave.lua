function SpiderClanCaveScreenPlay:respawnKiinDray()
	local pKiinDray = spawnMobile("dathomir", "kiin_dray", 0, -89.6619, -100.955, -102.623, 172, 3695712)

	if (pKiinDray ~= nil) then
		createObserver(STARTCOMBAT, "SpiderClanCaveScreenPlay", "spawnGuards", pKiinDray)
		createObserver(OBJECTDESTRUCTION, "SpiderClanCaveScreenPlay", "kiinDrayKilled", pKiinDray)
	end
end

function SpiderClanCaveScreenPlay:kiinDrayKilled(pKiinDray)
	createEvent(30 * 1000, "SpiderClanCaveScreenPlay", "respawnKiinDray", nil, "")

	return 1
end

function SpiderClanCaveScreenPlay:spawnGuards(pKiinDray)
	if (pKiinDray == nil or CreatureObject(pKiinDray):isDead()) then
		return 1
	end

	spatialChat(pKiinDray, "HiissSSshhh RaaAAAaaaa")

	for i = 1, #self.kiinGuards, 1 do
		local guardID = readData("kiinGuards:" .. i)

		local pGuard = getSceneObject(guardID)

		if (pGuard == nil or CreatureObject(pGuard):isDead()) then
			local guardData = self.kiinGuards[i]
			pGuard = spawnMobile("dathomir", "giant_cavern_spider_recluse", 0, guardData[1], guardData[2], guardData[3], guardData[4], guardData[5])

			if (pGuard ~= nil) then
				writeData("kiinGuards:" .. i, SceneObject(pGuard):getObjectID())
			end
		end
	end

	return 1
end
